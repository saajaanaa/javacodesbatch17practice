package class3;

public class E1TypeCasting {
    public static void main(String[] args) {
        byte age=12;
        int myAge=age;
        System.out.println(myAge);

        float num1=15.6f;
        int num2;
        num2 =(int)num1;
        System.out.println(num2);

        double number1=500.4444;
        long number2;
        number2=(long)number1;
        System.out.println(number2);






    }
}
