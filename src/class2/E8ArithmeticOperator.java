package class2;

public class E8ArithmeticOperator {
    public static void main(String[] args) {
        double num1= 22;
        double num2= 5;

        double addResults=num1+num2;
        double subResults=num1-num2;
        double mulResults=num1*num2;
        double divResults=num1/num2;
        System.out.println("The Addition of two numbers "+num1+" and "+num2+" is equal to " +addResults);
        System.out.println("The Subtraction of two number  "+num1+" and "+num2+" is equal to "+subResults);
        System.out.println("The multiplication of two number  "+num1+" and "+num2+" is equal to "+mulResults);
        System.out.println("The division of two number  "+num1+" and "+num2+" is equal to "+divResults);
    }

}
