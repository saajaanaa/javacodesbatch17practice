package class2;

public class VariableValues {
    public static void main(String[] args) {
        byte age=28;
        age=30;
        System.out.println(age);

        short distanceRoad=2345;
        distanceRoad=3455;
        System.out.println(distanceRoad);

        int salary=200000;
        salary=300000;
        System.out.println(salary);

        long population=234455667788L;
        population=345677889990L;
        System.out.println(population);

        float height=34.3F;
        height=36.6F;
        System.out.println(height);

        double weight=169.4567788888;
        weight=130.456788889;
        System.out.println(weight);

        char gender='M';
        gender='F';
        System.out.println(gender);

        boolean news=true;
        news=false;
        System.out.println(news);




    }
}
